<template>
  <div
    id="app"
    :class="
      typeof weather.main != 'undefined' && weather.main.temp > 16 ? 'warm' : ''
    "
  >
    <main>
      <div class="search-box">
        <input
          type="text"
          class="search-bar"
          placeholder="Search..."
          v-model="query"
          @keypress="fetchWeather"
        />
      </div>
      <div class="weather-wrap" v-if="typeof weather.main != 'undefined'">
        <Location
          :city="weather.name"
          :country="weather.sys.country"
          :date="dateBuilder()"
        />
        <Weather
          :temperature="Math.round(weather.main.temp)"
          :weather="weather.weather[0].main"
        />
      </div>
    </main>
  </div>
</template>

<script>
import Location from "./components/Location.vue";
import Weather from "./components/Weather.vue";
export default {
  name: "App",
  components: {
    Location,
    Weather
  },
  data() {
    return {
      api_key: "60a0526a2377dd50692490338efb326c",
      url_base: "https://api.openweathermap.org/data/2.5/",
      query: "",
      weather: {}
    };
  },
  methods: {
    fetchWeather(e) {
      if (e.key == "Enter" && this.query != null) {
        fetch(
          `${this.url_base}weather?q=${this.query}&units=metric&APPID=${this.api_key}`
        )
          .then(res => res.json())
          .then(data => {
            this.weather = data;
          });
      }
    },
    dateBuilder() {
      let date = new Date();
      let months = [
        "January",
        "February",
        "March",
        "April",
        "May",
        "June",
        "July",
        "August",
        "September",
        "October",
        "November",
        "December"
      ];
      let days = [
        "Sunday",
        "Monday",
        "Tuesday",
        "Wednesday",
        "Thursday",
        "Friday",
        "Saturday"
      ];
      return `${days[date.getDay()]} ${date.getDate()} ${
        months[date.getMonth()]
      } ${date.getFullYear()}`;
    }
  }
};
</script>

